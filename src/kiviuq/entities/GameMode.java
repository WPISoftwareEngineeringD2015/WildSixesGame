package kiviuq.entities;

/**
 * Enumeration determining mode of the game
 */
public enum GameMode {
	/** Game is set to Puzzle Mode */
	Puzzle,
	
	/** Game is set to Elimination Mode */
	Elimination,
	
	/** Game is set to Lightning Mode */
	Lightning,
	
	/** Game is set to Release Mode */
	Release;
}
